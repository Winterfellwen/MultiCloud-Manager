function init(locale) {
  _locale = locale || wx.getStorageSync('lang') || 'zh'
  wx.setStorageSync('lang', _locale)
}

function getLocale() {
  return _locale
}

function t(key, params) {
  var text = (translations[_locale] && translations[_locale][key]) || (translations['zh'] && translations['zh'][key]) || key
  if (params) {
    for (var k in params) {
      text = text.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k])
    }
  }
  return text
}

function switchLang(locale) {
  if (locale !== 'zh' && locale !== 'en') return
  init(locale)
  setTabBarLang()
  return _locale
}

function setTabBarLang() {
  var tabs = ['home', 'ai', 'resources', 'profile']
  var keys = ['nav.home', 'nav.ai', 'nav.resources', 'nav.profile']
  tabs.forEach(function(key, i) {
    wx.setTabBarItem({
      index: i,
      text: t(keys[i])
    })
  })
}

function getLangData(keys) {
  var data = {}
  for (var i = 0; i < keys.length; i++) {
    data[keys[i].replace(/\./g, '_')] = t(keys[i])
  }
  return data
}

module.exports = {
  init: init,
  getLocale: getLocale,
  t: t,
  switchLang: switchLang,
  setTabBarLang: setTabBarLang,
  getLangData: getLangData
}
